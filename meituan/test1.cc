class Solution {
public:
    int dir[4][2] = {{-1, 0}, {1, 0}, {0, -1}, {0, 1}};

    int numIslands(vector<vector<char>>& grid) {
        int n = grid.size();
        int m = grid[0].size();
        int res = 0;
        int cnt = 0;
        
        for(int i = 0; i < n; ++i) {
            for(int j = 0; j < m; ++j) {
                if(grid[i][j] != '1') continue;
                
                queue<pair<int, int>> bfsq;
                bfsq.emplace(i, j);
                grid[i][j] = '2'; // 入队的时候修改已访问
                while(!bfsq.empty()) {
                    auto [starti, startj] = bfsq.front();
                    bfsq.pop();
                    
                    for(int dirindex = 0; dirindex < 4; ++dirindex) {
                        int nexti = starti + dir[dirindex][0];
                        int nextj = startj + dir[dirindex][1];
                        if(nexti >= 0 && nexti < n && nextj >= 0 && nextj < m && grid[nexti][nextj] == '1') {
                            bfsq.emplace(nexti, nextj); 
                            grid[nexti][nextj] = '2'; // 入队的时候修改已访问
                            ++cnt;
                        }
                    }
                }
                
                ++res;
            }
        }
        cout << "访问次数： " << cnt << endl;
        return res;
    }
};

